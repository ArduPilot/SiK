// calculate the CRC16 of a buffer
// this costs about 2.2 microseconds per byte
uint16_t  crc16(uint8_t n, uint8_t * buf)
{
    register uint8_t k;
    register uint8_t high, low;

    high = low = 0;

    while (n--) {
        register uint8_t b = *buf++;
        k = high << 1;
        if (high & 0x80) {
            high = low ^ crc_tab2[k++];
            low = b ^ crc_tab2[k];
        } else {
            high = low ^ crc_tab1[k++];
            low = b ^ crc_tab1[k];
        }
    }
    return (((uint16_t)high)<<8) | low;
}
